#!/bin/bash

# Input numbers
num1=5
num2=2

# Calculate sum
sum=$((num1 + num2))

# Calculate difference
diff=$((num1 - num2))

# Calculate product
product=$((num1 * num2))

# Calculate quotient (integer part)
quotient=$((num1 / num2))

# Print the results
echo "Sum: $sum"
echo "Difference: $diff"
echo "Product: $product"
echo "Quotient: $quotient"
